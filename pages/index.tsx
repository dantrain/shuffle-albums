import type { NextPage } from "next";
import Head from "next/head";
import Image from "next/image";
import useSWR from "swr";
import "twin.macro";
import fetcher from "../utils/fetcher";

const useAlbums = () => {
  const { data, error } = useSWR<{
    items: {
      album: {
        id: string;
        name: string;
        images: { url: string; width: number; height: number }[];
      };
    }[];
    total: number;
  }>("/me/albums?limit=50", fetcher);

  console.log(data?.total);

  return {
    albums: data?.items.map((item) => item.album),
    isLoading: !error && !data,
    isError: error,
  };
};

const Albums = () => {
  const { albums, isLoading, isError } = useAlbums();

  if (isError) return <p>Failed to load</p>;
  if (isLoading) return <p>Loading...</p>;

  return (
    <ul tw="grid grid-cols-10 gap-4">
      {albums?.map((album) => {
        const image = album.images[0];

        return (
          <li key={album.id}>
            <Image
              src={image.url}
              alt={album.name}
              width={image.width}
              height={image.height}
            />
          </li>
        );
      })}
    </ul>
  );
};

const Home: NextPage = () => {
  return (
    <div>
      <Head>
        <title>Spotify Albums</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <h1 tw="mb-6 text-2xl font-bold">Spotify Albums</h1>

        <div tw="my-3">
          <Albums />
        </div>
      </main>
    </div>
  );
};

export default Home;
